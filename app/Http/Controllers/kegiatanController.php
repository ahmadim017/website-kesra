    {
        $validation = Validator::make($request->All(),[
            "nama" => "required",
        ])->validate();

        $kegiatan = new kegiatan;
        $kegiatan->nama = $request->get('nama');
        $kegiatan->save();

        return redirect()->route('kegiatan.index')->with('status','Data Berhasil disimpan');
    }

